package practice;

public class ArraysNotes {
    public static void main(String[] args) {


        String[] months = {
                "Enero",
                "Febrero",
                "Marzo",
                "Abril",
                "Mayo",
                "Junio",
                "Julio",
                "Augusto",
                "Septiembre",
                "Octubre",
                "Noviembre",
                "Deciembre"
        };

        System.out.println(months);
        for (int i = 0; i < months.length; i++) {
            System.out.println(months[i]);
        }

    }

}
